    public Tecnico() {
        initComponents();
        user = Login.user;
        sesion_usuario = Administrador.sesion_usuario;
        
        
        setSize(550, 300);
        setResizable(false);
        
        setLocationRelativeTo(null);
        setTitle("Tecnico sesion de -" + user);
        //// este if determina si la sesion esta iniciada por un administrador  es decir que si la condicion de mas abajo no se cumple eso quere decir que corresponde a un capturista
        if (sesion_usuario == 1){
            setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
        }else{
            setDefaultCloseOperation(WindowConstants.EXIT_ON_CLOSE);
        }
        // metodo que cambia el fondo de pantalla de la aplicacion 
        ImageIcon wallpaper = new ImageIcon("src/images/wallpaperPrincipal.jpg");
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(jLabel1_walpaper.getWidth(), jLabel1_walpaper.getHeight(), Image.SCALE_DEFAULT));
        jLabel1_walpaper.setIcon(icono);
        this.repaint();
        
        try {
            Connection cn = Conexion.Conectar();
            String ConsultaSql = "select nombre_usuario from usuarios where username ='"+ user + "'";
            PreparedStatement pst = cn.prepareStatement(ConsultaSql);
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                nombre_usuario = rs.getString("nombre_usuario");
                jLabel1_nombreUsuaurio.setText("bienvenido "+ nombre_usuario);
                
            }
        } catch (SQLException e) {
            System.err.println("error al obtener el usuario para el label nombre de usuario en la vista Tecnico " + e);
        }
        
    }
    
