    private void jButton_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_registrarActionPerformed
        // TODO add your handling code here:
        int validacion = 0;
        String tipo_equipo, marca, modelo, num_serie, dia_ingreso, mes_ingreso, ano_ingreso, estatus, observaciones;
        
        
        tipo_equipo = cmb_tipo_equipo.getSelectedItem().toString();
        marca = cmb_marcas.getSelectedItem().toString(); /// investigar esto  
        modelo = txtmodelo.getText().trim();
        num_serie = TxtSerie.getText().trim();
        observaciones = jTextPane_observaciones.getText();
        estatus = "Nuevo Ingreso";
        
        //// registro de fechas
        
        Calendar calendar = Calendar.getInstance();
        
        dia_ingreso = Integer.toString(calendar.get(calendar.DATE));
        mes_ingreso = Integer.toString(calendar.get(calendar.MONTH));
        ano_ingreso = Integer.toString(calendar.get(calendar.YEAR));
        
        if (modelo.equals("")){
            txtmodelo.setBackground(Color.red);
            validacion ++; 
        }
        if (num_serie.equals("")){
            TxtSerie.setBackground(Color.red);
            validacion ++;
  
        }
         if (observaciones.equals("")){
             
             // camr por regla de negocio de Asomado Soft 
            jTextPane_observaciones.setText("Sin Observaciones ");
            
        }
         if (validacion == 0 ) {
             
             try {
                 Connection cn = Conexion.Conectar();
                 String alpeo = "insert into equipos values (?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
                 PreparedStatement pst = cn.prepareStatement(alpeo);
                 
                 pst.setInt(1, 0);
                 pst.setInt(2, IDcliente_update);
                 pst.setString(3, tipo_equipo);
                 pst.setString(4, marca);
                 pst.setString(5, modelo);
                 pst.setString(6, num_serie);
                 pst.setString(7, dia_ingreso);
                 pst.setString(8, mes_ingreso);
                 pst.setString(9, ano_ingreso);
                 pst.setString(10, observaciones);
                 pst.setString(11, estatus);
                 pst.setString(12, user);
                 pst.setString(13, "");
                 pst.setString(14, "");
                 
                 pst.executeUpdate();
                 cn.close();
                 
                 TxtnombreCliente.setBackground(Color.yellow);
                 txtmodelo.setBackground(Color.yellow);
                 TxtSerie.setBackground(Color.yellow);
                 
                    JOptionPane.showMessageDialog(null, "Registro Exitoso");
                    this.dispose();
                 
             } catch (SQLException e) {
                 System.err.println("error en registrar equipo " +e);
                 JOptionPane.showMessageDialog(null, "contacte al administradoe hay un error ");
             }
            
        } else {
             JOptionPane.showMessageDialog(null, "debe de llenar todos los campos ");
        }
        
        
        

    }//GEN-LAST:event_jButton_registrarActionPerformed

