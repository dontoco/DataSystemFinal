    public InformacionUsuario() {
        initComponents();
        user = Login.user;
        user_update = GestionarUsuarios.user_update;
        setLocationRelativeTo(null);
        setResizable(false);
        setTitle("informacion del usuario " + user_update + " Secion de " + user);
        setSize(630, 450);

        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);

        /// cambiar icono del programa 
        ImageIcon wallpaper = new ImageIcon("src/images/wallpaperPrincipal.jpg");
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(jLabel1_Walpaper.getWidth(), jLabel1_Walpaper.getHeight(), Image.SCALE_DEFAULT));
        jLabel1_Walpaper.setIcon(icono);
        this.repaint();
        // esta linea informa el usuari oque se esta modoficiando 
        jLabel_titulo.setText("Informacion del usuario " + user_update);
        //// aqui se muestra la informacion del usuario 
        try {
            Connection cn = Conexion.Conectar();
            String ConsultaSql = "select * from usuarios where username = '" + user_update + "'";
            PreparedStatement pst = cn.prepareStatement(ConsultaSql);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                ID = rs.getInt("id_usuario");

                TxtNombre.setText(rs.getString("nombre_usuario"));
                Txt_mail.setText(rs.getString("email"));
                TxtTelefono.setText(rs.getString("telefono"));
                TxtUsername.setText(rs.getString("username"));
                TxtRegistradopor.setText(rs.getString("registrado_por"));

                CMB_niveles.setSelectedItem(rs.getString("tipo_nivel"));
                CCMB_Estatus.setSelectedItem(rs.getString("estatus"));

            }
            cn.close();

        } catch (SQLException e) {
            System.err.print("error cargando usuarios " + e);
            JOptionPane.showMessageDialog(null, "contacte al adiminstrador");

        }

    }

