    private void jButton1_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_ActualizarActionPerformed
        // TODO add your handling code here:
        int permisos_cmb;
        int estatus_cmb;
        int validacion = 0;

        String Nombre;
        String mail;
        String Telefono;
        String username;

        String permisos_String = "";
        String estatus_String = "";

        mail = Txt_mail.getText().trim();
        Nombre = TxtNombre.getText().trim();
        Telefono = TxtTelefono.getText().trim();
        username = TxtUsername.getText().trim();

        permisos_cmb = CMB_niveles.getSelectedIndex() + 1;
        estatus_cmb = CCMB_Estatus.getSelectedIndex() + 1;

        if (mail.equals("")) {
            Txt_mail.setBackground(Color.red);
            validacion++;

        }
        if (Nombre.equals("")) {
            TxtNombre.setBackground(Color.red);
            validacion++;

        }
        if (Telefono.equals("")) {
            TxtTelefono.setBackground(Color.red);
            validacion++;

        }
        if (username.equals("")) {
            TxtUsername.setBackground(Color.red);
            validacion++;

        }
        if (validacion == 0) {
            //esto trasforma el combobox de int a string 
            if (permisos_cmb == 1) {
                permisos_String = "Administrador";
            } else if (permisos_cmb == 2) {
                permisos_String = "Capturista";
            } else if (permisos_cmb == 3) {
                permisos_String = "Tecnico";
            }

            /// estatus CBM 
            if (estatus_cmb == 1) {
                estatus_String = "Activo";
            } else if (estatus_cmb == 2) {
                estatus_String = "Inactivo";
            }

            try {
                /// aqui se valida si el usuario que se esta actualizando existe en la BD 
                Connection cn = Conexion.Conectar();
                String ConsultaSQLValidaExistenciaDeUsuario = "select username from usuarios where username= '" + username + "' and not id_usuario = '" + ID + "'";
                PreparedStatement psr = cn.prepareStatement(ConsultaSQLValidaExistenciaDeUsuario);
                ResultSet rs = psr.executeQuery();

                if (rs.next()) {

                    TxtUsername.setBackground(Color.red);
                    JOptionPane.showMessageDialog(null, "nombre de usuario no disponible");
                    cn.close();

                } else {

                    Connection cn2 = Conexion.Conectar();
                    String ConsultaSQLparagrabar = "update usuarios set nombre_usuario=?, email=?, telefono=?, username=?, tipo_nivel=?, estatus=? where id_usuario = '" + ID + "'";
                    PreparedStatement psr2 = cn2.prepareStatement(ConsultaSQLparagrabar);

                    psr2.setString(1, Nombre);
                    psr2.setString(2, mail);
                    psr2.setString(3, Telefono);
                    psr2.setString(4, username);
                    psr2.setString(5, permisos_String);
                    psr2.setString(6, estatus_String);
                    System.out.println(estatus_String);
                    System.out.println(ID);

                    psr2.executeUpdate();
                    cn2.close();
                    JOptionPane.showMessageDialog(null, "usuario actualizado exitosamente");

                }

            } catch (SQLException e) {
                System.err.print("error al actualizar " + e);
            }

        } else {
            JOptionPane.showMessageDialog(null, "debeles llenar todos los campos ");
        }


    }//GEN-LAST:event_jButton1_ActualizarActionPerformed

