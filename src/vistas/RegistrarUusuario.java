    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        int permisos_cmb = 0;
        int validacion = 0;
        String nombre = "";
        String mail = "";
        String telefono = "";
        String username = "";
        String pass = "";

        String permisos_string = ""; // permiso String es para el combo box 

        mail = Txt_Mail.getText().trim();
        nombre = Txt_Nombre.getText().trim();
        telefono = Txt_Telefono.getText().trim();
        username = Txt_Username.getText().trim();
        pass = Txt_Password.getText().trim();
        permisos_cmb = CBM_Niveles.getSelectedIndex() + 1;

        if (mail.equals("")) {
            Txt_Mail.setBackground(Color.red);
            validacion++;
        }
        if (nombre.equals("")) {
            Txt_Nombre.setBackground(Color.red);
            validacion++;
        }
        if (telefono.equals("")) {
            Txt_Telefono.setBackground(Color.red);
            validacion++;
        }
        if (username.equals("")) {
            Txt_Username.setBackground(Color.red);
            validacion++;
        }
        if (pass.equals("")) {
            Txt_Password.setBackground(Color.red);
            validacion++;
        }
        /// estructura condicional para ver el valor que genera el comboBox 

        if (permisos_cmb == 1) {
            permisos_string = "Administrador";
        } else if (permisos_cmb == 2) {
            permisos_string = "Capturista";

        } else if (permisos_cmb == 3) {
            permisos_string = "Tecnico";
        }

        /// validacion de usuarios existente en las base de datos 
        try {
            Connection cn = Conexion.Conectar();
            PreparedStatement psr = cn.prepareStatement(
                    "Select username from usuarios where username = '" + username + "'");
            ResultSet rs = psr.executeQuery();

            if (rs.next()) {
                Txt_Username.setBackground(Color.red);
                JOptionPane.showMessageDialog(null, "Nombre de usuario no disponible");
                cn.close();

            } else {
                cn.close();

                if (validacion == 0) {
                    try {

                        Connection cn2 = Conexion.Conectar();
                        String ConsultaSQL = "insert into usuarios values (?,?,?,?,?,?,?,?,?)";
                        PreparedStatement pst2 = cn2.prepareStatement(ConsultaSQL);
                        pst2.setInt(1, 0);
                        pst2.setString(2, nombre);
                        pst2.setString(3, mail);
                        pst2.setString(4, telefono);
                        pst2.setString(5, username);
                        pst2.setString(6, pass);
                        pst2.setString(7, permisos_string);
                        pst2.setString(8, "Activo");
                        pst2.setString(9, user);

                        pst2.executeUpdate();
                        cn2.close();

                        Limpiar();

                        Txt_Mail.setBackground(Color.GREEN);
                        Txt_Username.setBackground(Color.GREEN);
                        Txt_Password.setBackground(Color.GREEN);
                        Txt_Nombre.setBackground(Color.GREEN);
                        Txt_Telefono.setBackground(Color.GREEN);

                        JOptionPane.showMessageDialog(null, "el registro fue exitoso");
                        this.dispose();

                    } catch (SQLException e) {
                        System.err.println("error en la vista regostro " + e);
                        JOptionPane.showMessageDialog(null, "Ocurrio un error en le registro , contacte al administraor del sistema ");
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Debe llenar todos los campos ");
                }

            }
        } catch (SQLException e) {
            System.err.println("ocurrio un error en la consulta SQL " + e);
            JOptionPane.showMessageDialog(null, "error al validar el usuario consulte con el administrador");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

