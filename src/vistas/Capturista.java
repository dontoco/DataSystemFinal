    private void jButton1_ImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_ImprimirActionPerformed
        // TODO add your handling code here:
        Document documento = new Document();
        try {
            String ruta = System.getProperty("user.home");
            PdfWriter.getInstance(documento, new FileOutputStream(ruta + "/Desktop/listaCliente.pdf"));
            
            
            com.itextpdf.text.Image header = com.itextpdf.text.Image.getInstance("src/images/BannerPDF.jpg");
            //se dimenciona imagen 
            header.scaleToFit(600, 1000);
            header.setAlignment(Chunk.ALIGN_CENTER);
            
            Paragraph parrafo = new Paragraph();
            parrafo.setAlignment(Paragraph.ALIGN_CENTER);
            parrafo.add("Lista de clientes. \n \n");
            parrafo.setFont(FontFactory.getFont("Tahoma",18, Font.BOLD, BaseColor.DARK_GRAY));
            
            documento.open();
            documento.add(header);
            documento.add(parrafo);
            
            PdfPTable tabla = new PdfPTable(5);
            tabla.addCell("ID");
            tabla.addCell("Nombre");
            tabla.addCell("Email");
            tabla.addCell("Telefono");
            tabla.addCell("Direccion");
            
            try {
                String Sebatian = "Select * from clientes";
                Connection cn = Conexion.Conectar();
                PreparedStatement pst  = cn.prepareStatement(Sebatian);
                ResultSet rs = pst.executeQuery();
                
                
                if (rs.next())
                {
                    do {      
                         tabla.addCell(rs.getString(1));
                         tabla.addCell(rs.getString(2));
                         tabla.addCell(rs.getString(3));
                         tabla.addCell(rs.getString(4));
                         tabla.addCell(rs.getString(5));
                         
                        
                    } while (rs.next());
                    
                    documento.add(tabla);
                }
            } catch (SQLException e) {
                System.err.println("Error al conectar con tabla clientes(lista de clientes  "+ e );
            }
            
            documento.close();
            JOptionPane.showMessageDialog(null, "lista de clientes generada correctamente");
            
        } catch (Exception e) {
            
            System.err.println("Error al generar pDF  "+ e );
        }
    }//GEN-LAST:event_jButton1_ImprimirActionPerformed

