    public GestionarEquipos() {
        initComponents();
        user = Login.user;
        setSize(630, 330);
        setResizable(false);
        setTitle("Capturista - Sesion de " + user);
        setLocationRelativeTo(null);

        /// esto impide que el programa se cierre cuando finaliza esta interfas
        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
        
        ImageIcon wallpaper = new ImageIcon("src/images/wallpaperPrincipal.jpg");
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(jLabel1_walpaper.getWidth(), jLabel1_walpaper.getHeight(), Image.SCALE_DEFAULT));
        jLabel1_walpaper.setIcon(icono);
        this.repaint();
        
        try {
            Connection cn = Conexion.Conectar();
            String conySQL = "select id_cliente, nombre_cliente, mail_cliente, tel_cliente, ultima_modificacion from clientes";
            PreparedStatement pst = cn.prepareStatement(conySQL);
            
            ResultSet rs = pst.executeQuery();
            
            jTable1_Clientes = new JTable(model);
            jScrollPane1.setViewportView(jTable1_Clientes);
            
            model.addColumn(" ");
            model.addColumn("Nombre");
            model.addColumn("Email");
            model.addColumn("Telefono");
            model.addColumn("Modificado por..");
            
            while (rs.next()) {
                Object[] fila = new Object[5];
                for (int i = 0; i < 5; i++) {
                    fila[i] = rs.getObject(i + 1);
                    
                }
                model.addRow(fila);
            }
            cn.close();
            
        } catch (SQLException e) {
            System.err.println("error en el llenado de la tabla" + e);
        }
        
        jTable1_Clientes.addMouseListener(new MouseAdapter() {
            
           @Override
           public void mouseClicked(MouseEvent e){
               int fila_point = jTable1_Clientes.rowAtPoint(e.getPoint());
               int columna_point = 0;
               
               if (fila_point  > -1) {
                   IDclienteUpdate = (int)model.getValueAt(fila_point, columna_point);
                   Informacion_cliente Caliente = new Informacion_cliente();
                   Caliente.setVisible(true);
                   
                   
                   
                   
                   
                   
               } else {
               }
           }
        });
        
    }
    
